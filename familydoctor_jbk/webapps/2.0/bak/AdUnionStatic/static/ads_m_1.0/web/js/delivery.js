function clickTxt(i,linkid,contid,bg,t){
	for(n=1;n<=t;n++){
		var contObj = document.getElementById(contid+n);
		var linkObj = document.getElementById(linkid+n);
		//alert(contObj);
		//alert(linkObj);
		if(contObj!=null && linkObj!=null){
			if(i==n){
				contObj.style.display="block";
				linkObj.className=bg;
			}else{
				contObj.style.display="none";
				linkObj.className="";
			}
		}
		
	}
}
